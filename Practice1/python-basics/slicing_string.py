#Slicing Strings
#example1
b = "Hello, World!"
print(b[2:5])  #output: llo

#example2 Slice from the start
b = "Hello, World!"
print(b[:5])  #output: Hello

#example3 Slice to the end
b = "Hello, World!"
print(b[2:])  #output: llo, World!

#example4 Negative indexing
b = "Hello, World!"
print(b[-5:-2])  #output: orl

#ex1 What will be the result of the following code?
x = "Welcome"
print(x[3:5])
#answer: co

#ex2 Get the characters from index 2 to index 4 (llo).
txt = "Hello World"
x = txt[2:5]
#answer: txt[2:5]

#ex3 What will be the result of the following code?
x = "Welcome"
print(x[3:])
#answer: come

