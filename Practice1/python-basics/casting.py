#Python Casting

#example1 Integers
x = int(1)
y = int(2.8)
z = int("3")
print(x)  #output: 1
print(y)  #output: 2
print(z)  #output: 3

#example2 Floats
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)  #output: 1.0
print(y)  #output: 2.8
print(z)  #output: 3.0
print(w)  #output: 4.2

#example3 Strings
x = str("s1")
y = str(2)
z = str(3.0)
print(x)  #output: s1
print(y)  #output: 2
print(z)  #output: 3.0

