#Assign Multiple Values

#example1

x, y, z = "Orange", "Banana", "Cherry"

print(x)  #output: Orange
print(y)  #output: Banana
print(z)  #output: Cherry

#example2

x = y = z = "Orange"
print(x)  #output: Orange
print(y)  #output: Orange
print(z)  #output: Orange

#example3

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits

print(x)  #output: apple
print(y)  #output: banana
print(z)  #output: cherry

