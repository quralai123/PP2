#Comparison Operators

#example1
x = 5
y = 3
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

#example2 Chaining comparison operators
x = 5
print(1 < x < 10)
print(1 < x and x < 10)

#ex1 What is the result of 5 == 5?
#answer: True

#ex2 Which operator checks if two values are NOT equal?
#answer: !=

#ex3 What does the expression 1 < x < 10 check?
#answer: If x is between 1 and 10

#ex4 Use the correct comparison operator to check if 5 is not equal to 10.
if 5 != 10:
    print("5 and 10 is not equal")
#answer: !=
